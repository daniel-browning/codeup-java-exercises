public class ControlFlowExercises {
    public static void main(String[] args) {
for (int i = 5; i <= 15; i++) {
    System.out.print(i + " ");
}

for (int n = 100; n >= -10; n -= 5) {
    System.out.println(n + " ");
}

for (long x = 2; x < 1000000; x *= x) {
    System.out.println(x * x + " ");
}
    }
}
